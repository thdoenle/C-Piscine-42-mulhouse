#include "rush02.h"

char	get_next_char(int fd)
{
	char	buf[1];

	if (read(fd, buf, 1) != 1)
		return (0);
	if (*buf < '\t' || (*buf > '\r' && *buf < ' ') || *buf == 127)
		return (-1);
	return (*buf);
}
