#include <unistd.h>

void	print_sol(char tab[11][11], int n)
{
	int	r;
	int	c;

	r = 1;
	while (r <= n)
	{
		c = 1;
		while (c <= n)
		{
			write(1, tab[r] + c, 1);
			if (c == n)
				write(1, "\n", 1);
			else
				write(1, " ", 1);
			c++;
		}
		r++;
	}
}
